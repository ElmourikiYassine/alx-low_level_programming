#include <stddef.h>
/**
 * op_add - add operation
 *
 * @a: integer a
 * @b: integer b
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - add subtitution
 *
 * @a: integer a
 * @b: integer b
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - add multiplication
 *
 * @a: integer a
 * @b: integer b
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide operation
 *
 * @a: integer a
 * @b: integer b
 * Return: div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo operation
 *
 * @a: integer a
 * @b: integer b
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}

